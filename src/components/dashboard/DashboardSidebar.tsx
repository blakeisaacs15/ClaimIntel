export default function DashboardSidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const [email, setEmail] = useState<string | null>(null);
  const [practiceName, setPracticeName] = useState<string | null>(null);
  const [initials, setInitials] = useState("CC");

  useEffect(() => {
    supabase.auth.getSession().then(({ data: { session } }) => {
      if (session?.user) {
        const userEmail = session.user.email ?? null;
        const practice = session.user.user_metadata?.practice_name ?? null;
        setEmail(userEmail);
        setPracticeName(practice);
        if (practice) {
          const words = practice.trim().split(" ");
          const ini = words.length >= 2
            ? words[0][0] + words[1][0]
            : words[0].slice(0, 2);
          setInitials(ini.toUpperCase());
        } else if (userEmail) {
          setInitials(userEmail.slice(0, 2).toUpperCase());
        }
      }
    });
  }, []);

  const handleSignOut = async () => {
    await supabase.auth.signOut();
    sessionStorage.clear();
    router.push("/sign-in");
  };

  return (
    <aside className="w-56 bg-slate-900 flex flex-col h-screen flex-shrink-0 border-r border-white/[0.06]">
      <div className="px-5 py-5 border-b border-white/[0.08]">
        <ClaimIntelLogo size={28} textClassName="text-slate-100" textSizeClassName="text-base" />
      </div>

      <nav className="flex-1 px-3 py-4 space-y-0.5 overflow-y-auto">
        {navItems.map((item) => {
          const isActive = item.href === "/dashboard"
            ? pathname === "/dashboard"
            : pathname.startsWith(item.href);

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all ${
                isActive
                  ? "bg-teal-500/20 text-teal-300 ring-1 ring-teal-400/30"
                  : "text-slate-100 hover:text-white hover:bg-slate-800"
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="px-4 py-4 border-t border-white/[0.08]">
        <div className="flex items-center justify-between gap-2">
          <div className="flex items-center gap-3 min-w-0">
            <div className="w-8 h-8 bg-teal-500 rounded-full flex items-center justify-center text-white text-xs font-bold flex-shrink-0 ring-2 ring-teal-400/30">
              {initials}
            </div>
            <div className="min-w-0">
              <p className="text-sm font-medium text-slate-100 truncate">
                {practiceName || "My Practice"}
              </p>
              <p className="text-xs text-slate-100 truncate">{email ?? ""}</p>
            </div>
          </div>
          <button
            onClick={handleSignOut}
            title="Sign out"
            className="text-slate-100 hover:text-white transition-colors flex-shrink-0"
          >
            <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
          </button>
        </div>
      </div>
    </aside>
  );
}